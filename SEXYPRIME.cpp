#include <bits/stdc++.h>
typedef long long ll;
typedef long double ldb;
typedef unsigned long long ull;
using namespace std;

const ldb PI = 3.1415926535897932384626433832795;
const ll INF = 1000000000000000000;
const ll MOD = 1000000007;
const ll MOD2 = 1000000009;
const ldb EPS = 1e-6;

ll a[10000];
bool check[1000000];

void sieve() {
	ll cnt = 0;
    for(ll i = 2; i * i < 1000000; i++) {
		if(check[i] == 0) {
            for(ll j = i * i; j < 1000000; j += i) {
                check[j] = true;
            }
        }
    }
	for(ll i = 11; i < 1000000; i++) {
		if(check[i] == 0 && check[i - 6] == 0) {
            if(cnt < 10000) {
                a[cnt] = i - 6;
                ++cnt;
            }
            else break;
        }
    }
}
void solve() {
    ll t; cin >> t;
    sieve();
    while(t--) {
        ll n; cin >> n;
        cout << a[n - 1] << " " << a[n - 1] + 6 << endl;
    }
}

int main() {
    // #ifndef ONLINE_JUDGE
    // freopen("SEXYPRIME.INP", "r", stdin);
    // freopen("SEXYPRIME.OUT", "w", stdout);
    // #endif
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    solve();
    return 0;
}

