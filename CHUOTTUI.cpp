#include <bits/stdc++.h>
typedef long long ll;
typedef long double ldb;
typedef unsigned long long ull;
using namespace std;

void solve() {
    ll x1, x2, v1, v2; cin >> x1 >> v1 >> x2 >> v2;
    bool ok = false;
    for(int i = 0; i <= 10000; i++) {
        x1 += v1;
        x2 += v2;
        if(x1 == x2) {
            ok = true;
            break;
        }
    }
    if(ok) cout << "YES";
    else cout << "NO";
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    solve();
    return 0;
}

