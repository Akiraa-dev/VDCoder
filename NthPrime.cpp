#include <bits/stdc++.h>
typedef long long ll;
typedef long double ldb;
typedef unsigned long long ull;
using namespace std;

const ldb PI = 3.1415926535897932384626433832795;
const ll INF = 1000000000000000000;
const ll MOD = 1000000007;
const ll MOD2 = 1000000009;
const ldb EPS = 1e-6;

void solve() {
    ll limit = 10000000;
    vector<int> lp(limit + 1);
    vector<int> pr;
    for(int i = 2; i <= limit; i++) {
        if(lp[i] == 0) {
            lp[i] = i;
            pr.push_back(i);
        }
        for(int j = 0; i * pr[j] <= limit; j++) {
            lp[i * pr[j]] = pr[j];
            if(pr[j] == lp[i]) {
                break;
            }
        }
    }
    ll t; cin >> t;
    while(t--) {
        ll n; cin >> n;
        cout << pr[n - 1] << endl;
    }
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    solve();
    return 0;
}

