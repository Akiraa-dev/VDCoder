void solve() {
    int n; cin >> n;
    int a[n];
    a[0] = 1;
    ll sum = 1;
    for(int i = 1; i < n; i++) {
        a[i] = a[i - 1] + 3;
    }
    ll res = 0;
    for(int i = 0; i < n; i++) {
        res += a[i];
    }
    cout << res << endl;
    for(int i = 0; i < n; i++) cout << a[i] << " ";
}

